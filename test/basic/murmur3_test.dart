Map<String, dynamic> sortMap(Map<String, dynamic> map) {
  final keys = map.keys.toList()..sort();
  final _map = <String, dynamic>{};
  for (var key in keys) {
    _map[key] = map[key] is Map ? sortMap(map[key]) : map[key];
  }
  return _map;
}

